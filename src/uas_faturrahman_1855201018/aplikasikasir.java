    public void beli() {
        koneksi konek = new koneksi();
        konek.koneksi();

        updatestok ubah = new updatestok();
        int totalbeli = Integer.parseInt(totalpembelian.getText());
        int stoklama = Integer.parseInt(stbarang.getText());
        int biaya = Integer.parseInt(stbarang2.getText());
        String nm = stbarang1.getText();

        int biaya1 = totalbeli * biaya;

        String asterik = "";

        asterik += " anda membeli " + nm + " uang yang harus di bayarkan = " + biaya1;

        asterik += "\n";

        if (stoklama < Integer.parseInt(out_brg.getText())) {
            ubah.update(Integer.parseInt(out_brg.getText()), stoklama - totalbeli);
            JOptionPane.showMessageDialog(null, asterik, "total pembayaran",
                    JOptionPane.INFORMATION_MESSAGE);
        }
  


        table();
    }

