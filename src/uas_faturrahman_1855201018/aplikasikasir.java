    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel(); //create a model
        int selectedRowIndex = jTable1.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        koneksi konek = new koneksi();
        konek.koneksi();

        try {

            String sql = "select * from datatoko where nomorbarang like '%" + id + "%'";
            pst = (Statement) konek.con.prepareStatement(sql);
            ResultSet rs = rs = pst.executeQuery(sql);

            while (rs.next()) {    //u can use normal query
                out_brg.setText(rs.getString("nomorbarang"));
                stbarang.setText(rs.getString("stokbarang"));
                stbarang1.setText(rs.getString("namabarang"));
                stbarang2.setText(rs.getString("hargabarang"));
            }
        } catch (Exception ex) {
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

