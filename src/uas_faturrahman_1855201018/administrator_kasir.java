    private void ad_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ad_cariActionPerformed
        if (pilih.getSelectedIndex() == 1) {

            try {
                selectBynobr(Integer.parseInt(in_nobr.getText()));
            } catch (SQLException ex) {
                Logger.getLogger(administrator_kasir.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (pilih.getSelectedIndex() == 2) {
            selectBynmbr(in_nmbr.getText());
        } else if (pilih.getSelectedIndex() == 3) {
            selectByjnbr(in_jnsbr.getText());
        } else if (pilih.getSelectedIndex() == 4) {
            selectByhrgbr(Integer.parseInt(in_hrgbr.getText()));

        } else if (pilih.getSelectedIndex() == 5) {
            selectBystbr(Integer.parseInt(in_stbr.getText()));

        } else {
            table();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_ad_cariActionPerformed

