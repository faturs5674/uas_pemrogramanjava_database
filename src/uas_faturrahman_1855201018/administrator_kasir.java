    public void selectBynobr(int nobr) throws SQLException {
        koneksi konek = new koneksi();
        konek.koneksi();

        try {
            String sql = "select * from datatoko where nomorbarang like '%" + nobr + "%'";
            pst = konek.con.prepareStatement(sql);
            rs = pst.executeQuery(sql);
            adm_tabel.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

